void main(){
  //        initialization
  //while -> while(condition){
  //                statements
  //                incre/decre
  //               }
  int i = 0;
  while( i < 10){
    i++;
    print(i);
  }

  print('do while');

  int j = 0;
  do{
    print(j);
    j++;
  }while(j <= 10);
}
/// j = 0   print j = 0  j++ = 1   1 == 10